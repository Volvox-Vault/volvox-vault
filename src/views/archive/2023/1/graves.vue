        <h3>KAILUM GRAVES</h3>
        <p>
          mrkailumgraves{@}gmail.com
          <br />
          Kailum is critically obsessed with the Web and born-digital content.
          He is particularly interested in image-rich technologies and the way
          global media communication—a landscape controlled by a handful of
          multidimensional oligopolistic corporate-run networks—can be sampled,
          organised, and considered in new philosophical, sociological, and
          political terms. Nonetheless, while these issues are political and
          economic in nature, Kailum believes anti-capitalist art offers no real
          alternative to the economic and ideological discourses of
          multinational capitalism. In its place, he is interested in examining
          the politics of the image and the construction of truth. To do this,
          he uses the Internet, which has normalised the act of collecting and
          compiling information, to preserve and curate found images and raw
          material. The aim is to engage with the cultural space and aesthetics
          of the Internet—and the vast amount of digital information it
          contains—as a subject, material, and tool of artistic production.
          <br /><br />
          To do this, Kailum investigates different media and photographic
          methods—from appropriation to pixel and data manipulation—to create
          still and moving image projects that reflect the influence of
          technology on the photographic medium and sit resolutely within the
          context of future movements of contemporary photography. One of the
          most convincing aspects of his approach is the way he urges the viewer
          to evolve from a condition of mere spectatorship to a position where
          they must actively reflect on the various roles of technology in our
          media-driven age. However, rather than openly expressing
          socio-political criticism, Kailum takes a gentler
          ‘hint-at-the-direction’ path which invites the viewer to a process of
          reflection. On the surface his work might seem easy to read, but on
          closer inspection a viewer will find that he draws upon a myriad of
          artistic, philosophical, scientific, theoretical, and
          cross-disciplinary approaches to achieve his highly refined poetic
          ends. <br /><br />
          Kailum’s research delicately exists in a space between photography,
          Internet art, algorithmic art, and digital performance. He uses a
          laptop the same way a photographer uses a camera to document, record,
          and manipulate the world—which for Kailum is born-digital artefacts—to
          explore the disappearance of clear boundaries between culture,
          environment, and technology. He is particularly interested in the way
          humans use technology to transform, manipulate, and transcend natural
          environmental limits. There isn’t a one-way correlation; instead, a
          culture develops a technology, such as agriculture, to change the
          environment, and this technology, in turn, changes the culture and
          society that created it. Kailum explores this closed loop by creating
          large-scale still and moving images that play with the ubiquitous
          dichotomies between physical and digital existence.
        </p>
