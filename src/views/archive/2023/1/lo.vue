        <p>
          <strong
            >Did anyone specific become critical to the creation of your film?
            Why do they stand out as significant to your film?</strong
          >

          <br /><br />
          The film was largely inspired by Chris Marker's Sans Soleil, because
          of its way of treating the flow of fictional narratives, and Rhayne
          Vermette's Domus, because of its visual-collage sensibilities.
          <br /><br />
          In class, we were lead by Franci Duran for which brought in multiple
          guests, talking about their process and the details of their
          treatments. This idea of 'setting rules for yourself', a manifesto, I
          was very interested in and later wrote about. <br /><br />
          From that I wrote my own to get out of my head. A text titled the
          Noise Manifesto: in short the message embraces maximalism to overcome
          the procrastination from perfectionism. A lot of people I saw in film
          school were stuck to film-the perfect idea, the text goes against it
          and prompts to film every idea for the project, for which me and my
          DOP Alex Curren did. Around 1/3 of the footage made it into the film.
          <br /><br />
          <strong
            >How did you perceive films as a kid, in comparison to how you look
            at them now?</strong
          >
          <br /><br />
          I think when I was a kid, films are almost more emotional compared to
          viewing it as an adult. A large part of it is the naïvety and the
          misunderstanding of a narrative mixed with imagination. <br /><br />
          For example, when I was around 7-8 or so, I would go to my uncles
          place to watch Back to the Future (1,2, and 3) back to back, over and
          over. There are a lot of things I didn't understand about the film:
          the language, certain words, the majority of the plots, the taboo/
          ethics of incest, the logic of time travel... All of that is
          abstracted by 60%, filled in with the feeling of watching it and the
          imagination of kid-me filling in the gaps. So, in short, it was much
          more magical, similar to the adrenaline I had listening to Dubstep in
          the van as a teenager. In some sense I'm envious of that feeling, but
          as substitute I could now enjoy the films of Michael Haneke.
          <br /><br />
          <strong
            >What emotion or human experience were you most drawn to in the
            making of this film and why?</strong
          >
          <br /><br />
          I was most drawn to the feeling of great anxiety and dread; a sense of
          a perpetual identity crisis in flux between two land geographically
          opposite to each other. All of this came to a point of melancholia in
          retrospect, at least to me. <br /><br />
          Above that I'm also interested in how others experience the film
          emotionally, its like a collaboration between the audience and the
          filmmaker given that the film could be abstract and completely
          subjective at times. <br /><br />
          <strong
            >What is your writing process like (or are you more into
            improv)?</strong
          >
          <br /><br />
          I find my own writing process tedious and unnecessarily complicated,
          hence I'm always envious of those that could work in a more linear and
          a more reasonable way. <br /><br />
          It always starts out as frantic, several yellow post-its on white
          wall. Abstract thesis and scattered ideas. I always write two concepts
          and draw an arrow between them, like so, "Homeland - - - Identity".
          Looks stupid but it simplifies those grand ideas for me. <br /><br />
          Eventually I would pick some ideas from that tree of post-its and
          write a script or a treatment, not so much for myself but for
          organisational purposes and for other collaborators to understand the
          project so they know what to do. <br /><br />
          Personally I've gave up on an inherent structure (i.e introduction,
          climax, resolution and so on), this will always differ from project to
          project, but an arc kinda follows once bigger scenes formulates.
          <br /><br />
          Once we got the elements of the scene, the specificity of those ideas
          will form via improv on the day of shooting. That's the driving force
          of 'Why do ants go back to their nest?' and continues for my newest
          project. <br /><br />
          Nowadays, I'm trying to make my life easier by writing a script.
          <br /><br />
          <strong>How do you approach making a personal film?</strong>
          <br /><br />
          I find auto-fiction a great way to approach this, inspired by the
          works of Ann Quin, Kurt Vonnegut and Sarah Kane. <br /><br />
          It brings be comfort that I could disguise certain aspect of reality,
          or talk about what I care about without being up front. To distort the
          self in a personal film is to make the self almost more visible. The
          more absurd (but truthful) the better to me. For that reason I find
          Leos Carax's reoccurring character Merde fascinating. <br /><br />
          Moreover, I like to preserve my privacy as an individual, I don't like
          to be fetishised from identity/ expression of identity, so, making up
          things is crucial for me to be more truthful.
        </p>
