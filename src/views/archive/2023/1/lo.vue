        <h3>ALEX LO</h3>

        <p>
          alexlo1037@gmail.com<br />
          I started making this film around September 2022 when I was 22 as part
          of my thesis project at York University. Being the final film to be
          made in film school, there's a sense of a rite of passage, a statement
          piece to sum up who I am as an artist (at least up until then).
          Subsequently I had been offered to be in the Alternative Class rather
          than the fiction class, which offered a much wider range of freedom in
          what I could do and opened up the possibility of anarchy. So I tried
          something I haven't prior, to entirely fuck up my way of making films
          and ditch linear storytelling.
          <br /><br />
          At the beginning there were no specific ideas, only some vague thing
          for a 5 person dance film or a Harmony Korine mixed with Maya Deren
          aesthetic thing, but after some time it became too abstract to grasp
          what I wanted to do. Before this film I had only made absurd,
          other-worldly films, it was largely because of my status of being an
          international student, I didn't know how to relate to a Canadian
          expression, it was hard to think about how to capture the reality of
          this place or even questioning if I have anything to say about Canada
          for Canadians. This sense of alienation in itself I found conflicting
          yet interesting, I wanted to spotlight that, so it ended up being the
          film: a pseudo-diary film about myself coming from Hong Kong, and my
          brief summary of time spent here (along side with other tangents).
          <br /><br />
          To be frank, I thought the film was “disgustingly personal” even
          though in retrospect it didn't reveal much detail on who I am. I
          wasn't interested at all in making myself the subject matter as it
          could easily be cynical or too solipsistic. But, it's easier to
          schedule things. This aspect of the self I took inspiration from the
          films of Agnes Varda; in how she treats things in relation to herself
          and how it makes her feel, yet its not really a self-portrait but
          still a commentary. <br /><br />
          A central idea that the film gravitates towards is how home links to
          identity, and how that disconnect disorientates the filmmaker. The
          title 'Why do ants go back to their nest?' reflects that, it comes
          from the studies about how ants could retrace (able to home) even
          though they had been displace to a "novel location". I wanted to
          reframe this behavioural phenomenon to people, and why someone would
          return to 'home' (or where their from) despite of the environment's
          radical/ political change.
        </p>
