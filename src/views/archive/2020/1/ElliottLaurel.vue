        <p>
          <mark class="sub">Final Thoughts</mark>
        </p>

        <br />
        <p>
          <mark class="q"
            >Tiana: Any recommendations for people just starting out with HTML
            or building servers?</mark
          ><br />
          <mark class="laurel">Laurel says:</mark><br />
          While it might make sense to delay, I actually think it’s important to
          start using real web servers as soon as possible to truly understand
          how to build a website that’s fully online, and not some “toy
          project.”
          <br /><br />
          However, I also think it’s good to recognize that a website can still
          be a website even if it’s not publicly online, similar to how our HTML
          Energy guest
          <a href="https://html.energy/larissa.html" target="_blank"
            >Larissa Pham</a
          >
          made her first websites at her grandparents’ house. Larissa said, “I
          got into coding websites because I wanted to make a fan site for this
          manga series. None of it ever went live, but I learned how to write
          everything in a a notepad file (like a text file) and just save it
          with the `.html` extension and open it in my local browser. So I would
          mock up websites that way. I would read source code from other
          people’s sites to learn how to do things.” So in other words, a
          website is just a set of files, and one of those files should be
          readable by a web-browser (like the extension `.html`)—and it could
          even live only on your computer and still be a website. Larissa shows
          that you can enjoy making a website that is only for you… it doesn’t
          have to go online. <br /><br />
          It’s up to you whether or not to put your website online (that is,
          from your computer to a web-server). However, in my biased opinion, I
          think putting your website online, on a real web-server, is part of
          the essential magic of websites.
          <br /><br />
          With my students and clients, I usually recommend people start with a
          basic paid host (which rents you web-server space) such as
          <a href="https://www.nearlyfreespeech.net/" target="_blank"
            >Nearly Free Speech</a
          >. This service is very clear about how it charges you. There are also
          no year-long binding contracts… it’s kind of like a gas meter, you put
          in money and it gradually runs out. So you can truly calculate how
          much your site costs you per day, if you like. It allows for PHP,
          which is useful if you have a Wordpress, Kirby, or some dynamic
          content. On the other hand, I think
          <a href="https://pages.github.com/" target="_blank">Github Pages</a>
          is a good route for small, static websites, and it’s technically free…
          although note it’s owned by Microsoft.
        </p>

