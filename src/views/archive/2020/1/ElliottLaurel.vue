        <p>
          <mark class="sub">HTML Energy</mark>
        </p>

        <br />
        <p>
          <mark class="q"
            >Tiana: What is
            <a href="http://html.energy/" target="_blank">HTML Energy</a>?</mark
          ><br />
          <mark class="laurel">Laurel says:</mark><br />
          It’s an ongoing conversation and movement about the famous “HTML,”
          also known as HyperText Markup Langauge. It’s also a podcast where
          we’ve talked to several web geniuses about how they go about creating,
          updating, and living their websites.
          <br /><br />
          <mark class="elliott">Elliott says:</mark><br />
          Yeah, I like to think of it as a movement or fan club. I remember the
          name came from a conversation I had with Laurel once about energy. I
          asked her if she thought everything had energy and she said yes. So I
          started wondering if something nonphysical like a language could have
          energy. It’s nice to think of HTML as a means of transferring and
          storing energy between people.
        </p>

        <br />
        <p>
          <mark class="q"
            >Tiana: What power does accessibility hold for you in relation to
            web building?</mark
          ><br />
          <mark class="laurel">Laurel says:</mark><br />
          We like fast and lightweight websites. Making websites with only HTML
          enables this because HTML is universal to all websites. It’s the
          foundation. Unfortunately in 2020, a lot of people forget that, for
          example, adding unnecessary JavaScript that’s only for superficial
          polish actually adds a lot of weight to pages, sometimes making them
          unable to load on slow connections. This is especially sad when the
          website contains important, life-sustaining information that should be
          accessible to all humans.<br />
          With HTML Energy’s podcast, so far we've been talking to mostly
          individual designers and programmers, but we would love to talk with
          someone from a more government or public service organization
          perspective working on websites that optimize for people in areas with
          low bandwidth connections, for example. Let us know if you think of
          anyone.
        </p>

        <br />
        <p>
          <mark class="q"
            >Tiana: How have the conversations on HTML Energy impacted
            you?</mark
          ><br />
          <mark class="laurel">Laurel says:</mark><br />
          By interviewing people, we connect with other special, unique
          energies. — I love hearing how different each energy’s approach is.
          Especially now in 2020, there are so many tools out there to make a
          website, and honestly it can be overwhelming, and to boot, there can
          be a lot of
          <a href="https://www.are.na/block/7447577" target="_blank"
            >social pressure</a
          >
          around using tools that are currently trendy. So when we interview
          people, we get to hear specifically how they have used simple HTML and
          made websites in their own unique way. I love hearing the specific
          details and the origin stories especially. For example,
          <a href="https://html.energy/tom.html" target="_blank">Tom Bubul</a>
          made Dungeon and Dragons fan sites in the 90s. He didn’t even play the
          game, just invented his own scenarios as his way to play. And other
          people we interviewed such as
          <a href="https://html.energy/emma.html" target="_blank"
            >Emma Rae Norton</a
          >
          didn’t start coding websites until she was in college and still found
          her own creative way of approaching it for the first time by slowly
          coding by hand and embracing that slowness. Hopefully hearing these
          stories shows how anyone can approach HTML as a pliable and expressive
          creative medium… similar to more traditional mediums like clay, paper,
          paint, or even a musical instrument. And unlike most technology which
          is fast and efficient, such as anything we use quickly on our phones,
          we should actually consider embracing the clunkiness, slowness, and
          imperfection of HTML. Our guest
          <a href="https://html.energy/korede.html" target="_blank"
            >Korede Aderele</a
          >
          embraces “bugs” or things that don’t work as originally intended. “I
          figure it’s my personal website so I can do what I want with it. I’m
          fine with having a bug sit there for a while. Sometimes, I just leave
          a bug and let that become a weird, quirky feature.” <br /><br />
          Before HTML Energy started, I was very inspired by website hand-coder
          Zach Mandeville, who declares,
          <a
            href="https://coolguy.website/writing/basic-html-is-the-new-punk-folk-explosion.html"
            target="_blank"
            >“Basic HTML Is the New Punk Folk Explosion,”</a
          >
          saying making websites by hand is similar to older folk traditions.
          <br /><br />
          After we launched the podcast, at first we thought no one was
          listening. We had no way of knowing immediately since we didn’t put it
          on Soundcloud and didn’t have analytics on our pages… so we couldn’t
          see the play count or anything. But after a few months, we heard that
          a few people had listened to all the episodes, and were enjoying it.
          This delayed feedback feels nourishing somehow. The time elapsed
          between a message and its reply is an important part of the message
          itself.
        </p>

