        <p>
          <mark class="sub">Vastness</mark>
        </p>

        <br />
        <p>
          <mark class="q"
            >Tiana: Why is it important that we design with vastness in mind
            when building a website?</mark
          ><br />
          <mark class="laurel">Laurel says:</mark><br />
          I think it has to do with transparency and wanting to show the
          underlying structure and not obscure things.
          <br /><br />
          <mark class="elliott">Elliott says:</mark><br />
          Yes, I think transparency is definitely important. I have a strange
          relationship with design because on one hand it can be a really
          important and useful tool for organizing information but it can also
          easily get in the way. An example of this is when people design for
          the web like they would for print. Things like web fonts and layout
          are nice but just get in the way if they don’t support the underlying
          content. Maybe a good analogy is a public library. In a library all
          the bookcases are designed to present books (information) without
          obscuring their titles or how many there are. Most modern websites
          feel like libraries with a few books on a table and all the other
          books tucked out of sight in storage. There might be a card catalog
          system for looking up titles, but sometimes that’s even missing. This
          seems like a big mistake in the long run because it’s putting design
          and engagement before information, exploring, and play.
        </p>

        <br />
        <p>
          <mark class="q"
            >Tiana: How have you incorporated vastness into your work since
            writing
            <a
              href="https://elliott.computer/pages/exploring-the-vastness-of-a-website.html"
              target="_blank"
              >Exploring the vastness of a website</a
            >?</mark
          ><br />
          <mark class="elliott">Elliott says:</mark><br />
          Most of my energy after writing that essay went into building the cafe
          and Special Fish. More recently, I’ve taken a step back and have been
          trying to observe more. After putting so much time into making things
          on the web, I’ve been feeling like I want to put more energy into
          things happening off the web. Maybe this goes back to your question
          before about how websites can impact real lives. I’m not sure what
          this would look like for me or for HTML Energy but it’s fun to think
          about.
        </p>

