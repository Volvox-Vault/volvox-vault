        <p>
          <mark class="sub">Community Websites</mark>
        </p>
        <br />
        <p>
          <mark class="q"
            >Tiana: Can you describe your fascination with community
            websites?</mark
          ><br />
          <mark class="elliott">Elliott says:</mark><br />
          I’m fascinated by what happens when you open up a private website to
          the public and allow people to create their own pages, etc. All these
          social networks and apps that we use on a daily basis are just
          websites with databases at their most basic level. I think sometimes
          people lose sight of that. The web hasn’t really changed all that much
          from a technical standpoint. I’m fascinated that a tiny community
          website running on a home server and a giant social media website
          share many of the same technologies. It’s kind of beautiful and makes
          me feel like nothing is set in stone–there’s always alternative ways
          of being on the web.
        </p>

        <br />
        <p>
          <mark class="q"
            >Tiana: What were your intentions with
            <a href="http://special.fish/" target="_blank">Special Fish</a>? How
            have they evolved?</mark
          ><br />
          <mark class="elliott">Elliott says:</mark><br />
          At first I was specifically trying to create an alternative social
          network. After it launched,
          <a
            href="https://webcache.googleusercontent.com/search?q=cache:u1dLIH6Eaf4J:https://www.kickscondor.com/special.fish+&cd=1&hl=en&ct=clnk&gl=us&client=firefox-b-d"
            target="_blank"
            >Kicks Condor</a
          >
          wrote about it on their website and called it “a big directory of
          people.” That really changed my perspective on the project. I realized
          my field of vision was too narrow. I ditched social media recently
          because it was training me to look at websites in a certain way.
          People talk a lot about social media’s adverse effects, but I don’t
          see this problem brought up. I suppose it only really impacts people
          building websites, but it leads to unfortunate outcomes nonetheless.
          So to answer your question, I think of Special Fish as a directory to
          explore the handmade web, websites that are hand crafted by real
          people and not by corporations. There are a lot of these sites out
          there but very few directories to find them, so Special Fish hopes to
          help with that.
        </p>

        <br />
        <p>
          <mark class="q"
            >Tiana: What is the relationship between your community websites and
            real life? How do these websites impact our real lives?</mark
          ><br />
          <mark class="laurel">Laurel says:</mark><br />
          I’ve been thinking recently about how I feel in social spaces—physical
          and digital alike. I love when I’m in a room with other people, and I
          am in awe of everyone around and somehow absolutely believe that
          everyone present is a special, kind genius. I could imagine this
          happening, for instance, at a lecture that a friend is about to give
          and I’m talking with some people there, and even though I don’t know
          everyone, I’m excited by and respectful of everyone in the space. I
          also feel this way about most people on Special Fish, too. I think
          it’s because so many people put such thought and creative energy into
          their profiles. But that’s only possible because Elliott took special
          care and intention with the design—which is not just how it looks but
          also how it functions, what it requires of users. The design of
          Special Fish encourages its participants to put this effort forward.
          It’s a reciprocal relationship.
          <br /><br />
          But then there are other social spaces, both physical and digital,
          where I think everyone is kind of trash and superficial. Not always,
          but often Instagram affords this, as does certain IRL events
          pre-COVID, like some gallery openings. I just look around and have
          this feeling like everyone sucks in some way, and even if that’s not
          true on an individual level… somehow the energy of the event and
          social space feels unthoughtful and unfortunately doesn’t make me
          excited to interact with others. Something about the social space is
          too easy and not thoughtful. Maybe ultimately these spaces are not
          “for us”—Instagram uses its users’ posts as advertising to earn money,
          and gallery openings are often perfunctory events meant for social
          climbing.
          <br /><br />
          <mark class="elliott">Elliott says:</mark><br />
          Agreed. I’m pretty glad that a lot of events have been canceled. At
          the beginning of quarantine, with so much communication switching to
          video chat, I think it was immediately evident that online could never
          really replace in-person interaction. There’s always some kind of
          energy missing when you’re on a call. It bothers me that these social
          networks are continually trying to replace in-person communication
          when they could be working on supporting preexisting forms of
          communication. I’d love to see more websites that support real world
          interaction. It was inspiring to see all these Google Docs being
          created during the recent Black Lives Matter protests. I think that’s
          a good example of websites impacting real lives.
        </p>

        <br />
        <p>
          <mark class="q"
            >Tiana: How do your community websites differ from popular social
            platforms?</mark
          ><br />
          <mark class="laurel">Laurel says:</mark><br />
          Since they’re more clearly authored, often homespun community
          websites’ goals, rules, and moderation are more obvious. This makes
          sense since most of them spun out of being dissatisfied by the larger,
          more popular social platforms.
          <br /><br />
          <mark class="elliott">Elliott says:</mark><br />
          Yeah the goals are pretty different when it comes to community website
          vs social media websites. One thing that separates them is the ability
          to experiment. When your metrics aren’t based on how long you can keep
          a user on a website, things really open up.
        </p>

        <br />
        <p>
          <mark class="q"
            >Tiana: With projects like
            <a href="http://gossips.cafe/" target="_blank">Gossip's Cafe</a>,
            you close the cafe at 11pm. <br />What is your philosophy behind a
            non-24/7 website?</mark
          ><br />
          <mark class="elliott">Elliott says:</mark><br />
          I’ve always felt that websites should have grounding in the world.
          Sometimes this can happen through a feature or design choice, but it
          could also be quite literal like having open hours. When I started the
          cafe, I had this idea of literally turning off the website/server at
          the end of the day. Sort of like turning off the lights in a real cafe
          and locking up. I guess the current implementation is not that
          drastic, so it might be cool to actually turn off the server in the
          future :)
          <br /><br />
          <mark class="laurel">Laurel says:</mark><br />
          Grounding a website in the world helps people understand that
          technology is man-made and literally comes from the earth. Like other
          things that come from the earth, it’s a resource that has limitations.
          Normally people understand websites to be up all the time, 24 hours a
          day, 7 days a week, because they imagine websites as ethereal and not
          tied to the physical world, when in fact they are.
          <br />
          This reminds me, I interviewed
          <a href="https://html.energy/marie.html" target="_blank"
            >Marie Otsuka</a
          >
          on HTML Energy about the solar-powered website she designed,
          <a href="https://solar.lowtechmagazine.com/" target="_blank"
            >Low-Tech Magazine</a
          >. She said, “Just because things are digital doesn’t mean they’re
          more sustainable. There are servers that are powered by fossil fuels
          that serve up these digital documents.” Her visual design works in
          ways to make this underlying truth more apparent.
        </p>

