    <details class="zoe"><summary>some writing about the film: <em>room in the bag of stars</em></summary>
      <div>
        <em>by Zoë Mills</em>
      <br><br>

Containers in the room with us: two Coors Banquets, a film camera shaped like a can of orange juice, chapstick. Ryan’s movie is a container, too. 
<br><br>
Ryan Steel is a filmmaker. He also DJs with impossible energy, wears sunglasses at night, and does projections for the band Fencing. Projections that make me feel as if I’m watching the show from inside a computer. And some great, omnipotent being is browsing that computer—opening too many tabs, scrolling Wikipedia, and exploring Google Maps on foot. 
<br><br>
I also can’t picture Portage Place without seeing Ryan’s head floating, wizard-like, above it. So, credit is due there, too. 
<br><br>
Ryan’s short film Late Summer is a container in the sense that, when watching it for the first time, I felt immediately contained. Surrounded by it in every direction. As if I had walked into the film and shut a door behind me.  
<br><br>
This may be due to the fact that nearly every scene, even the outdoor ones, were shot on set. 
<br><br>
A set, Ryan tells me, that was lit to resemble the dioramas at the Manitoba Museum. The exterior shot of the camp cabin was a tableau of miniatures constructed by Ryan’s younger brother. This scene would have had mountains in the background if it weren’t for a mouse infestation. 
<br><br>
Without the mountains, the cabin exterior more closely resembled the Manitoban and east-central summer camps of Ryan’s youth. His real-life camp memories constructed the framework for the film’s earliest scripts. Chapstick shortages, nearly conversional Christian rock bands, and getting pantsed. 
<br><br>
<strong>Are you ever surprised by the conclusions you're led to?</strong>
<br><br>
Originally, the film featured outdoor scenes, shot on location in Gimli with 16mm, hand-cracked film. Our unnamed protagonist (portrayed by Dylan Lillie-Brine) and 20 other kids frolicing in the water. Real water. But something wasn’t right. He had to go full-artifice.
<br><br>
What Ryan is led to, really, is anything that gives off the same energy as a public school play. Construction paper and plasticine. Creating whole worlds that can be torn down at the end of the night, or stored away or hurled into a dumpster if mice make a home in your mountains. 
<br><br>
He wants the most artificial thing. The weirdest camera. The key that unlocks a portal to another world. Hair sprayed lenses and toy store tech. The weirder the key the more magnificent the world. 
<br><br>
<strong>Do you ever come to a point where you wonder if anyone is going to understand it? </strong>
<br><br>
Quebecers in Switzerland think they understand. Maybe they do. Ambiguity is comforting. The camp counselor is to kissing what toucans are to Froot Loops. You can risk being misunderstood. You can take the plastic off the mattresses. You can return a thousand dollars’ worth of astroturf, no questions asked. But if you return over 300 dollars of Ikea stuff they will call the manager.  
<br><br>
With Late Summer, Ryan captures the camp experience the way you might examine an ant under a magnifying glass. He gives it his full attention and lets it do its thing. What unfolds is an uncannily nostalgic movie about a shy, pre-pubescent kid searching for connection at summer camp—that happened to be filmed in the winter, just days before the 2020 lockdown. 
<br><br>
<strong>When was the last time you made a snow angel?</strong>
<br><br>
Another container: a snowglobe with a house and a yard. Ten bucks to shovel the walk. For Ryan and his brother, it’s a good deal. But the old man who owns the house forgets where he put the money. He just keeps forgetting. For a moment the house, with its yard and unregulated porch, is theirs. At just 15 and 13, these brothers are homeowners. A call for celebration. Cannonballs off the porch. Snow angels in the banks. 
<br><br>
This is a magical snowglobe. And the old man must live outside of it. 
<br><br>
<strong>Are you scared?</strong>
<br><br>
Most of the time. Real Housewives. Birthday parties. AI. Ryan doesn’t fly with liquids.  
<br><br>
No, not that scared. It’s just a costume. Ryan tells ghost stories on the bus. It’s scary when it could be real. It’s funny then, too.
<br><br>
There is, I think, an anxiety within this film that is old and musty, like hand-me-downs dragged up from the basement. The make-believe is at once magical and unnerving. Familiarity is impossible. We’re never in the same place twice. We run, but we don’t go anywhere. 
<br><br>
<strong>Did the writing come easily?</strong>
<br><br>
There’s what you want to make and then there is what you are capable of making. Ryan praises M. Night Shyamalan. No one talks like that in real life, but this isn’t real life. It’s a movie. Aren’t you listening? 
<br><br>
Its sets aside, Late Summer feels like a container because something important has been placed inside of it for safekeeping.   
<br><br>
<strong>So it's getting harder?!</strong>
<br><br>
More containers
<br><br>
The biggest slot machine in the entire world. Pull the lever and the reel randomizes. You are inside this container and every spin rearranges your molecules. Enough to make you a new person?
<br><br>
A wooden jewellery box from the dollar store. Decorated with muddy paint and barely-sticky stickers. You can keep it forever. You can let yourself in. 
<br><br>
A 35mm film canister. How deliciously the cap fits into place. They’re spill proof. You can put just about anything in there. Liquids. Gels. A life.  
<br><br>
He thinks it’s getting easier.  
    
