              <p class="q">
                Tiana: How would you describe yourself as an artist?
              </p>
              <p><img
                src="https://cdn.glitch.com/31c7994a-ff45-4592-b835-14262460c504%2Fnatan.png?v=1621622042467"
                alt="natan lawson plotter painting."
