                Natan: I’m naturally drawn to observe and collect ephemera. These are
                the transient objects, textures, and printed matter we encounter
                in our daily lives that one might throw out, or simply overlook.
                The principles that make an image pleasing to the eye —
                composition, movement, contrast, scale, connection — are at work
                everywhere. I find that anything, from the template of a child’s
                cursive worksheet to the printed pattern on a paper plate, can
                often be appreciated for its formal qualities. While some might
                consider these things as insignificant or dismiss as outright
                trash, I find joy in letting my eye sort through the scraps to
                find those moments that speak to me as art. By collecting,
                documenting, and collaging these remnants into the primary
                source material of my paintings, I aim to monumentalize and
                celebrate the iconography in our everyday life. Since 2018 I’ve
                been using needle point as the primary source material in my
                paintings. There’s so much that I like about needlepoint: First
                of all, it has endless source material. (I’ve accumulated
                thousands of scans in an archive and keep collecting more).
                Second, it is relatable on an emotional level (reminding us of
                home/domesticity). I also find the iconography is a valuable
                carrier of ideas. Needlepoint straddles this weird space where
                the imagery can either relate to a personal connection (say,
                your cat) or the archetype, the idea of the thing itself. I
                think needlepoint iconography exists in our collective
                unconscious as a visual, common denominator. Finally,
                needlepoint has formal qualities of the grid and stitch that
                lends itself to working with a computer: thread count
                corresponds to pixel size and a set of coordinates from gcode
                naturally replicates a stitch of thread. In short, I like to
                look for inspiration in the scraps. I love noticing compositions
                and identifying little moments: the discarded bits, clippings,
                doodles, and still lifes that when cropped, scaled, and
                replicated, give it new attention and life through my
                perspective of noticing and honoring the richness in their
                original design.
              </p>

              <p class="q">
                Do you have any rituals for getting into your workflow? What
                does a day in the studio look, sound, and feel like for you?
              </p>
              <p>
                My schedule completely flipped when the pandemic hit and I went
                from running an event and catering business with no time to
                create art, to being locked down and having more time than I
                could have imagined to simply paint. Since April, I’ve been
                working in the studio four to six days per week, nine to sixteen
                hours at a time. I spend long days there because it takes many
                hours for my painting machine to finish a single color layer. I
                need to monitor the machine for paint refills and to service the
                moving parts. Through 6 years of personal trial and error, plus
                a lot of help from other DIY Computer Numerical Control (CNC)
                enthusiasts, I’ve finally developed a mechanical process that
                works well, about 90% of the time. While the machine is humming
                along, I work on other things like stretching canvas, watching
                YouTube, or daydreaming. I can even leave the studio to grab
                lunch or take a walk in the park and come back to it still
                painting, as if I’d never left. As luck would have it, the 10%
                of the time the machine does fail is when I decide to take a
                break.
              </p>

              <img
                src="./2018-04-04 15.04.19.jpg"
                alt="natan lawson plotter painting."
              />

              <p class="q">
                What came first for you, the computer or the paint brush? And
                how did they end up intersecting?
              </p>
              <p>
                During my Junior year studying for a painting BFA at RISD, I got
                really interested in printmaking. Particularly, I fell in love
                with monoprinting and screenprinting. I spent as many credits
                and as much time as I could in the Printmaking Department.
                However, I was effectively just interested in learning
                printmaking techniques to create paintings. I was drawn to
                printmaking’s surface quality and embrace of mediated processes.
                On the other hand, I wasn’t interested in creating editions and
                resisted being limited to working on paper. I fell into working
                with plotters and CNC machines by accident. In my senior year I
                was trying to figure out a more economical and sustainable way
                to screenprint, and a friend in the Industrial Design department
                suggested looking into a vinyl cutter to cut out positives. I
                bought one and experimented for a month with it, but in the end
                the savings weren’t worth the hassle and the results were
                disappointing. However, I was fascinated by the machine and the
                potential of its use in another project. My vinyl cutter was
                shipped with a tiny ballpoint pen that was supposed to be used
                not for pen plotting, but as a way to orient yourself with the
                cutter before you possibly ruin good vinyl material. I wanted to
                go beyond this small ballpoint pen but the machine was designed
                so nothing else could fit. I figured out that if I unscrewed the
                sidecaps of the cutter I could remove the top cross bar
                completely, which would allow me to use sharpies. I hot glued
                the LCD control panel to the side and was able to start really
                pushing the capabilities of the machine into pen plotter domain,
                and it quickly became my new obsession. The summer after I
                graduated, I collaborated with engineers, students, and a
                professor from Brown University. Together, we built a large
                painting machine that could actually hold a paint brush, use
                more colors, and could even wash out the brush between colors.
                After that I wanted to move on and build the next machine that
                would incorporate what I learned from that experience. That’s
                what led me to using the current machine in my studio work.
              </p>

              <p class="q">
                What is fascinating to you about creating hybrid medium art?
              </p>
              <p>
                I’m fascinated by the liberation in creative expression that
                this digitally controlled paint application process gives me. My
                head and my hand feel like they are working at synchronous
                speed. Ironically, while I can work so much faster than before,
                in many ways the hand is more creative than the head, and I miss
                the immediate satisfaction of observational drawing. The fact
                that the work is process oriented rather than results oriented
                is freeing. I also find it more conducive to achieving the
                specific aesthetic that I’m looking for. The automatic aspects
                of the process also allows me to spend time on other steps that
                interest me, and to see an idea through to the end without
                changing my mind mid-painting. Developing this process over the
                years has also taught me patience and forethought I never really
                used or needed in my painting process. I still get impatient,
                though. The machine can be unpredictable at times, but the
                uncertainty keeps me motivated to persevere. I am still eager to
                keep developing new methods and excited for the future
                possibilities it holds.
              </p>

              <img
                src="./2018-07-23 00.28.19 (1).jpg"
                alt="natan lawson plotter painting."
              />

              <p class="q">
                What is the process like with your plotter paintings and what
                tools are involved?
              </p>
              <p>
                I have a tendency to tinker with things and to build. In my
                process I have to reverse-engineer the painting. Since I start
                with the final image on the computer, I have to move backwards
                and figure out what needs to happen with the tools and the
                machine to create that work. It’s a very technical and fussy
                process, to go from digital image to physical painting. I start
                with a photograph, a scan, or a collage, and then each image
                moves through four different types of software. They are
                converted from jpegs to svgs, and eventually to gcode. For
                example, a six-color, one-panel painting has 20 files associated
                with its creation and execution. I have been getting better
                about organizing a folder structure. In order to remain in flow
                working at the studio, I need to be able to move from one color
                to the next, and it’s crucial to know where to find specific
                files, as I often have to make mid-painting edits to account for
                paint viscosity, design change, etc. The work always requires
                experimentation. Every time I make a decision to change the size
                or the materials, I hold something else constant, so I can learn
                how the machine and material will react.
              </p>

              <p class="q">
                Where could someone start if they’re interested in experimenting
                with plotted art?
              </p>
              <p>
                I would recommend looking into the AxiDraw from Evil Mad
                Scientist. Although I’ve never actually used it, it looks like
                the most user friendly option for beginners. I’ve used some of
                their software and kits before and built a 3’x4’ version of
                their Watercolorbot. They were open to the idea and supportive
                when I needed advice. I’d also recommend searching #plotterart
                online for inspiration of what’s possible. You’ll see it’s hard
                to create something that doesn’t look obviously plotted.
              </p>

              <img src="./2345_18.jpeg" alt="natan lawson plotter painting." />

              <p class="q">
                I love how your art has a simple aesthetic from afar, yet you
                can feel the complexity of it through the medium, and when you
                take a closer look to see each brush stroke and all the
                layers... it feels very balanced. Is there an intentional
                process behind this?
              </p>

              <p>
                Since it takes a lot of work to prepare each color, I place each
                layer with as much intention as if I had to burn a silkscreen
                screen for it. The machine paints by layering tiny marks
                gradually, which is noticible close up. I also put a lot of
                thought into how many colors I want to use, the width of the
                mark, and size of the canvas to try to find the balance that
                each piece needs. It’s something I’m still learning to play
                with. I secretly love making one-color paintings, and wish I was
                better at making two-color ones.
              </p>

              <p class="q">
                Do you have some general values for your art that influence it?
              </p>
              <p>
                Some of the key values that are conveyed in my work are
                recycling, attentiveness, playfulness, and joy. It’s not easy to
                pinpoint specific concepts, though. I think that the elements I
                appreciate in the work of others also carry through in my own
                work. What I value can be seen in the inspiration I draw from
                external sources. I’m interested in mark making,
                misregistration, layering, and distortion with printmaking
                processes like Sigmar Polke. I’m drawn to kitsch, the
                ready-made, and fabrication like in Jeff Koons’ work, but I want
                my work to be a hybrid of machine and hand. I’m trying to
                develop a more playful and varied reproduction aesthetic like
                Laura Owens. I would also like to deepen and develop the poetic
                aspects of my work. I draw inspiration from artists like Pierre
                Alechinsky, especially his monograph “Margin and Center.” His
                explanation of how a frame can tell the same story but at a
                different pace as the subject has been good food for thought
                while developing my current body of work.
              </p>

              <img
                src="./Natan 12 tripped out flower pattern.jpg"
                alt="natan lawson plotter painting."
              />

              <p class="q">
                How does working with a plotter influence your perception of
                when a piece is ‘done’?
              </p>
              <p>
                Before, layering, mishaps and “going with the flow” played a
                more important part in my work, which made it much harder to
                determine the exact point when a piece was finished. In a sense,
                with painting, I had no predetermined idea of what the final
                work would look like before I started. I also used to work on
                multiple paintings at once, sometimes even up to 40 at a time!
                In my current process, I start with a precise idea of the end
                product then trace my steps back. Then, the machine does its
                work. When there is a mishap, I either fix it by hand if it is
                small, or create new color layers in certain areas. When
                everything goes well, the automatic process allows the work to
                flow from what is decided in the beginning. A piece gets “done”
                once it gets stretched, and I tend to only stretch the ones I
                like, so I have a lot of unstretched pieces sitting around.
              </p>

              <p class="q">
                How does playfulness play a role in your art practice?
              </p>
              <p>
                Work with no play is no fun. It’s hard to sustain long term and
                make improvements. I remember an assignment in a drawing class
                where we had to create a drawing using an intermediary /
                interruption between us and our hands. My idea for the
                assignment was so absurd that it essentially forced me to give
                myself permission to play. For my project, I got a feeder mouse
                and called him Cheeto, the first mouse artist. Then I tied
                markers to his ball for him to drag around a large sheet of
                paper. I suppose you could call it my first painting “machine”,
                but I like to think of it as a collab ;) Nowadays I aim to
                create lighthearted work that doesn’t take itself too seriously.
                I like to play around a lot during the initial stages of the
                process: I have an archive of hundreds of collages I’ve made and
                I allow myself a lot of freedom in selecting, cropping, cutting,
                changing colors, and making series out of the images before I
                decide to turn one into a painting. I don’t filter myself at
                this early stage, and it helps me be sure of my choice down the
                road and improve the overall quality of the work.
              </p>

              <p class="q">
                What other mediums do you work with, and how are they special to
                you?
              </p>
              <p>
                Two months before my show “Home Sweet Home” was set to open, my
                painting machine broke down in a way I couldn’t diagnose or
                repair. The situation forced me to search for another way to
                create in a pinch, so I joined a local maker space to use their
                laser cutter. It was my first time using that machine, but with
                it I created my favorite pieces in the show: Mr. Plumbean’s
                House, out of colored plexiglass sheets, and Regard La Lune,
                from laser etched paper pulp on canvas. I haven’t been back
                there since, but making sculptures and etchings using a laser
                cutter has been on my mind ever since and I hope to do more of
                that in 2021.
              </p>

              <img src="./2345_24.jpeg" alt="natan lawson plotter painting." />

              <p class="q">
                What artistic project of yours has been the most memorable?
              </p>
              <p>
                I took a ceramics class one semester in college. Although I
                started out doing hand building, most of the work I created used
                items I found in the home goods section of a thrift store and
                used as molds. The experience of going to that store every week,
                letting myself be surprised and finding things I would have
                never otherwise paid attention to was fun and addictive. I was
                also taking a poetry writing course at the time, and wrote a
                poem to go with the ceramic work. I’m happy with that work, but
                it’s the poem that was most impactful for me. It’s been almost
                six years now, but I feel that text still speaks true to my art
                making intentions and was the seed of all the painting work
                that’s come after. I have it on my website.
              </p>

              <p class="q">
                What helps you recharge when you need a break from working?
              </p>
              <p>
                I have a hard time relaxing, but the leisure activities that
                recharge me are arranging my house, finding good deals on FB
                marketplace, and watching Jeopardy. My guilty pleasures are Tik
                Tok and Twitter, but I’d like to spend more time away from the
                screen.
              </p>

              <p class="q">Do you have any recommendations for our readers?</p>
              <p>Make sure to take breaks and go for a walk :)</p>

       
