            <p>
              The zodiac signs with which we are familiar have many meanings. My
              own sign, Libra, is a calendrical month (roughly from
              mid-September to mid-October). Libra also holds space for a
              complex of characteristics, which include fairness, balance, and a
              love of beauty. Libra is also a mythological reference to the
              Blind Scales of Justice and all the rich classical narrative
              behind that imagery. <br>

              While the stories and the cycles of the
              zodiac are quite old - at least as old as writing and agriculture
              - the modern twelve symbols are quite recent. Medieval, in fact.
              Our zodiacal alphabet comes from the alchemists. 
              
              <br>♈ ♉ ♊ ♋ ♌ ♍ ♎ ♏ ♐ ♑ ♒ ♓<br> 
              
              Alchemy is more than chemistry. Much more.
              Alchemy is the art and science of reflection on self-experience
              through the use of physical materials and chemical processes.
              Alchemists saw that whatever experiments they processed in their
              laboratories also affected changes within themselves. This has
              been lost in modern chemistry, but can serve us well even in a
              post-alchemical age. It can be immensely helpful to realize that
              when we change our world, we also change ourselves. All substances
              carry actionable metaphors that can give us a handle on our
              individual internal lives.<br>

              Gold, for alchemists, represented the
              bold and proud ego. Mercury - both planet and substance -
              represented the clear, clever, quick-thinking mind. So, in the
              lab, dissolving gold in a solution of mercury was an actionable
              metaphor that allowed the alchemist to dissolve their pride in the
              liquid of critical thought.<br>
              
              The ultimate goal for alchemists was,
              of course, to create the philosopher's stone from raw materials.
              This process is the magnum opus of a philosopher-chemist and, in
              most alchemical traditions, involved twelve steps. Yes, these
              twelve processes are the twelve zodiac. It is from this magickal
              science from which we receive our astrological symbolism.<br>
              
              Dear reader, I hope you will join me in the following exercise, in
              which we begin with the prima materia of a raw experience, and
              through the twelve zodiacal steps, transmute our sensory memory
              into crystal-clear revelation. In the words of John Dewey,
              experiential educator extraordinaire, we do not learn from
              experience. Rather, we learn from reflecting on experience. How to
              reflect on experience? Some simple chemistry, using the wheel of
              heaven as our chemical formula. To begin, choose an experience of
              yours that does not feel fully digested. Perhaps something
              traumatic, or ecstatic, or simply confusing.</p> 
              
              <h2>♈ Aries: Calcification</h2>
              <p>Ingredients: pen, paper</p>
              <p>1. Write or draw everything you
              can remember about your experience. Imagery, smell, dialogue.
              Completely empty your memory onto the page or pages. Quantity, not
              quality.<br> 
              2. Burn the pages and collect the ashes.<br>
              Calcification is
              the alchemical process of burning something until only ashes
              remain. Alchemists, remember, were only beginning to understand
              that things are made of other things, that organic and inorganic
              materials had anything in common, that certain compounds could be
              extracted and remixed with other compounds. Calcification is a
              very exciting and very simple way to transmute something into
              something else: stuff into burnt stuff!<br>
              The metaphor of starting
              with fire also feels appropriate to me. Echoes of Prometheus,
              first trickster and patron of human civilization. Echoes of the
              proto-human courting of the mysteries of fire, lost in geological
              time.</p>
              
              <h2>♉ Taurus: Coagulation</h2>
              <p>Ingredients: calcified ashes, water, coloring of your choice</p>
              <p>1. Find something colorful like flower
              petals, beet juice, dye, glitter powder, or mushed-up leaves. The
              coloring agent shall be added purposefully and with the intention
              of entangling a certain mood or emotion with the reflective
              process.<br>
              2. Mix the ashes and the coloring with water to make a
              paint.<br> 
              3. Look at the mixture and determine if any more color or
              elements are needed. Perhaps it is not blue enough, or needs a
              handful of soil.<br> 
              Taurus, coagulation, the first mixing. It is
              already a revelation to realize that not everything was lost in
              the calcification, even though the original form has been scorched
              to dust. The question emerges: what remains after a fire?<br> 
              This is
              a good step to add in any extra memories or entanglements around
              your experience. Remember, the open secret of alchemy is that the
              substance is simply a reflection of the self. As you pour or mix
              or blend in new elements, you are pouring them right into
              yourself. What do you need to continue this work?</p>
              
              <h2>♊ Gemini: Fixation</h2>
              <p>Ingredients: coagulated paint, thick paper, paintbrush</p>
              <p>1. Paint a circle.<br>
              2. Fill the circle with symbols that represent the
              elements of your experience. These do not have to be meaningful to
              anyone but you. A brushstroke takes up more space than a
              penstroke, so this step allows you to compress your initial
              experiences into simpler shapes and words.<br>
              Fixation, technically,
              is modifying a substance so that it is immune to flame. In our
              activity, we are modifying the memory of the experience so that it
              is less likely to dissolve in the mind. Symbols are more stable
              and memorizable than fleeting impressions, and so we paint boldly
              at the stage of Gemini.</p> 
              
              <h2>♋ Cancer: Dissolution</h2>
              <p>Ingredients: fixated painting, flour, scissors, water, jar</p> 
              <p>1. Shred your painting
              with scissors (or a food processor) and add flour and water until
              you have a thin pancake-batter paste.<br>
              2. Pour your mixture into a jar
              and speak your hopes and fears into the container. Are you hoping
              to incorporate this experience into your creative work? Are you
              worried that you will not be able to release the trauma of this
              experience from your heart? Let it be known.<br>
              3. Seal the jar.<br>

              After fixing our carefully-combined philosophical substance, we dissolve
              it. Dissolution, to me, feels like a release. A pause between
              intense activities and the heat of alchemical transcendence. I
              invite you, dear reader, to let the jar sit as long as you like,
              and enjoy your chosen memory dissolving in cool, patient water.<br>
              
              Terence McKenna has said that the alchemist is a hypnotist, but
              instead of using a swinging clock, he trances out on swirling
              colors dancing through glass tubes. Look long enough into the
              bubbling cauldron and the potion looks also into you.</p> 
              
              <h2>♌ Leo: Digestion</h2>
              <p>Ingredients: dissolved mixture, boiling pot, stove</p> 
              <p>1. Put the mixture on very low heat.<br>
              2. As the mixture thickens, stir
              regularly and give thanks to everyone and everything that has
              given you this experience. Speak their names, species, or ISBN
              numbers out loud.<br>
              3. Take notice of the smells emerging from the
              steam and heat. Do they trigger any memories? This alchemical brew
              is speaking back to you, just as a ouija board or tarot deck would
              do. Listen. When the doughy mixture is thick enough to mold, take
              it off the heat and let it sit until the dough is warm. In
              alchemy, digestion is the process of slowly heating a substance.
              Very much like human digestion, really, which is microbe-mediated
              oxidation over a long and gurgly time period. I imagine the
              process of digestion like the grumbling pre-roar of Leo.
              Confident, proud, and ready to move at any moment. Consider how
              strong you are to have come this far and experienced so many
              radiant days and haunted nights.</p> 
              
              <h2>♍ Virgo: Distillation</h2>
              <p>Ingredients: digested dough, plastic bag</p> 
              <p>1. Mold the dough into a
              form that represents your experience. Do not think about the form,
              rather, simply keep your hands moving and shaping until the piece
              feels finished. Whatever you come up with will be the correct
              shape. Feel free to add color or other objects to this sculpture
              if it feels right. Googly eyes are highly encouraged. Seal the
              sculpture in a plastic bag and leave it in a warm place for
              several days. As the sculpture dries, the moisture will collect in
              the bag. Continuously collect the moisture until the sculpture is
              completely bone-dry. Distillation! Extracting the ephemeral spirit
              from the crunchy body! A very exciting step - good work for coming
              this far! It only gets better from here. Remember, there is much
              more to distill in the world than just alcohol. Everything has an
              essence, or many entangled essences, and Virgo shows us how to
              find them. Perhaps you may start guessing at the viscosity and
              color and perfume of the distilled essence of a Sunday evening, or
              a day at the beach, or a long class period.</p> 
              
              <h2>♎ Libra: Sublimation</h2>
              <p>Ingredients: distilled moisture, local ecology</p>
              <p>Pour the moisture into the roots of a nearby plant, knowing that the water will be
              soaked up through the xylem and exhaled out into the world's
              breath. Take a piece of the plant with you, with appropriate
              respect. A handful will do fine. Sublimation is the chemical
              process in which a substance heats up and turns straight from a
              solid to a gas. Think about dry ice - that's sublimation. To
              psychology, sublimation is the act of engaging in "higher-level"
              behavior in order to satisfy needs. For the simple need of food,
              for instance, we begin as babies, screaming when we are hungry.
              Then, we sublimate that impulse, and learn how to ask for food.
              Later, we sublimate again, and practice the complexities of the
              kitchen in order to cook our own meals. The plant-clipping will
              continue with us as a representation of our experience being
              sublimated into higher levels of complexity. Moisture becomes
              plant life, and our raw experience folds into a more beautiful
              shape.</p>
              
              <h2>♏ Scorpio: Separation</h2> 
              <p>Ingredients: dry sculpture, pen, paper</p>
              <p>Put your sculpture in an otherwise empty pocket first thing
              in the morning. Throughout your day, crumble the sculpture into
              powder. As your sculpture falls apart, also feel your experience
              grinding into more discrete elements. Whatever lessons or
              understandings occur to you throughout your day are being
              communicated to you through the separation of the sculpture. Write
              them down on a fresh sheet of paper. The Scorpio process of
              separation acts like a centrifuge. Layered elements begin to
              emerge, each with their separate characteristics and lessons.
              During this process of sculpture-crumblings, you will feel ideas
              pop into your awareness that feel special and glowy. Perhaps you
              have a revelation about love, or forgiveness, sex, violence,
              trauma, family, or friendship. Record them all, in as short of
              sentences as possible.</p>
              
              <h2>♐ Sagittarius: Ceration</h2> 
              <p>Ingredients:
              sublimated plant matter, written lessons, separated sculpture,
              tape, jar</p>
              <p>Take the sheet of written understandings you have
              recently discovered and roll the paper into a tube. Fill the tube
              with the crumbled sculpture and seal both ends with tape (or
              staples, or glue). Crush up your plant matter into paste and
              spread this wet gloop onto the outside of your tube. Seal your
              glooped scroll in a jar. Ceration is the alchemical process of
              slowly adding liquid to a hard, heated substance. In our
              reflective process, the heat comes from the wisdom written in your
              rolled-up scroll. The plant paste, the sublimated fluidity of our
              experience, will begin to act on the dry dust of the scroll,
              infusing your lessons with life.</p>
              
              <h2>♑ Capricorn: Fermentation</h2>
              <p>Ingredients: cerated scroll, patience</p>
              <p>Leave your scroll in the jar
              for a long time - until something interesting starts to happen.
              Perhaps mold will grow, or the tube will shrivel. As the scroll
              ferments, allow the lessons you have learned to soak into your
              mind. Act on what you've learned. Put this nearly-finished
              philosopher's stone into action and allow it to influence your
              future experiences.</p>
              
              <h2>♒ Aquarius: Multiplication</h2>
              <p>Ingredients: fermenting scroll, paint</p>
              <p>Paint the outside of your jar with a
              single color. Cover the entire surface. Once the first layer of
              paint dries, illustrate each of the ten steps you have experienced
              in the creation of this magnum opus. Let dry, and rest.
              Multiplication is a mysterious process. In a ritual format,
              alchemists reserved this step for the recapitulation or
              re-enactment of the journey of the initiate. Multiplication allows
              you to collect all the raw experience of the process of reflecting
              on experience and reflect on the experience of collecting those
              experiences. This is a good time to think through all you have
              done and learned and wish to take with you into the future.
              Without this step, without Aquarius, alchemy would be an empty and
              unremembered chore. The gift of Aquarius is the gift of going
              forth fully armed with revelation. In the experiential education
              field, pioneered in many ways by John Dewey, this step is also
              crucial. The stage of multiplication is the process of turning an
              experience into a good story. A story that can be shared, enjoyed,
              and used by others to also transform themselves.</p>
              
              <h2>♓ Pisces: Projection</h2>
              <p>Ingredients: multiplied scroll, sunbeams, moonbeams</p>
              <p>Leave your jar in a windowsill that will receive both the sun and
              the moon. Project. The most secretive and mystical step of all is,
              of course, the last. Projection does not have an equivalent in
              modern chemistry. The idea is this: to publish, radiate, and
              conduct your completed work into the world. Your magnum opus is a
              physical substance just as much as it is the understanding you
              received along the way. They are one and the same: compound and
              cognition. Whatever this looks like to you, do it. Maybe you will
              smash the jar, bury it in scarred soil, or allow it to soak up the
              view of the window. Do it, and mean it.
            </p>

