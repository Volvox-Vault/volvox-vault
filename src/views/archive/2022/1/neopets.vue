              <p style="text-indent: 15px">
              Much like NFTs, and perhaps a little like cryptocurrency itself,
              Neopets pet trading is a speculative game. Changes in policy mean
              perceived value can rise and crash in an instant. Neopets, though,
              is 22 years old, practically ancient in terms of web standards.
              It’s a relic of the Web 2.0 before the blockchain, social media,
              and the algorithm as we know it today. What did people come on the
              internet for? A cursory look into the Neopets site will uncover
              feeding digital pets, training their stats in the battledome, and
              dressing them up. But what gives the website its staying power is
              also true of what gives social media, and supposedly, NFTs, its
