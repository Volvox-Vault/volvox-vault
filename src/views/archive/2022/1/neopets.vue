              Neopets, the community gathers on the
              NeoBoards. They are discussion boards where like-minded users can
              talk about the many aspects of the site - whether that’s roleplay,
              battling, or pet trading. Indeed, Web 2.0 operated this way.
              People sought others on the basis of specialized interests. Today,
              the algorithm plays a heavy hand in bringing people together
              through content, so it’s easy to forget that people are capable of
              finding one another just fine on their own. </p>
              
              <p style="text-indent: 15px">
              Like all platforms which have faced pressure to adapt to changing times, Neopets is
              no stranger to metamorphosis. In 2007, The Neopets Team (TNT for
              short) began sitewide changes to how users could interact with
              their digital pets. These changes continue to affect a lucrative
              black market economy, a rich language of trading, and a
              speculative crash. </p>
              
              <p style="text-indent: 15px">
              To engage in the Neopets community, you need a
              pet. You can either create a pet - which comes in four basic
              colours, blue, red, yellow, and green - or you can adopt pets from
              the pound. Pounded pets can come in all colours, including pets
              which are painted from rare in-game paintbrush items. The pets in
              the pound are abandoned by people who don’t want them anymore, and
              anyone is free to adopt pets in the pound. </p>
              
              <p style="text-indent: 15px">
              One of the major site
              features introduced in this time was the ‘transfer’ option in the
              pound. It ensured a secure peer-to-peer transfer of one pet for
              another on different accounts. Transfers are limited by account
              age - the older your account, the more transfers you’re allowed
              every month. If you wanted to move one pet from one account to
              another before the change, it was through a risky pound manoeuvre:
              the sender would tell the recipient what time their pet would be
              abandoned, and the recipient would just have to refresh like crazy
              when that time came and hope they were faster than whoever else
              could snap them up. </p>
              
              <p style="text-indent: 15px">
              NST is Neopian Standard Time, which is what
              site dailies use to reset. The introduction of the transfer
              feature meant users could now engage in regular, guaranteed pet
              trading. A lively community of pet trading enthusiasts quickly
              started to form on the Neopian Pound board, colloquially known as
              the Pound Chat, or PC. Instead of grinding out games for NeoPoints
              to buy an expensive paintbrush, why not just trade one of your
              pets for someone else who wants your pet?</p>
              
              <p style="text-indent: 15px">
              The community-run system
              of how this informal marketplace operates has developed an
              incredible sophistication. While TNT places policy restrictions on
              what you can and can’t do - one pet may be traded for one other
              pet only, no buying pets, no offering rare items or NeoPoints
              alongside a pet trade - values of pets are almost completely
              determined by the community. Different types of pets have
              different values. Users will selectively make trades which
              discriminate between deciding factors your pet possesses: name,
              age, species, paint job, and perhaps most importantly of all,
              conversion status. </p>

              <p style="text-indent: 15px">
              In the changes TNT implemented, their answer to
              one of the most requested features at the time - being able to
              dress up your pets - was a sitewide conversion to Adobe Flash,
              where previously dynamic pet artwork was made into standardized
              and customizable poses across 54 Neopet species. Pets used to be
              JPEGs drawn in different poses. It would be unnecessarily
              laborious to redraw an item of clothing hundreds of times to fit
              every combination of colour and species. TNT automatically
              converted most of the artwork to fit the new standardized poses,
              but they gave 227 combinations of pet colours the option to retain
              their old artwork. These pets with old artwork are called
              unconverted pets. </p>
              
              <p style="text-indent: 15px">
              It was a decision that made sense. Standardizing
              poses meant new wearable items could be coded into zones in Adobe
              Flash. Items could be layered according to what visually made
              sense. A background goes behind a hat, which goes over makeup. It
              also means custom tweaking for how different types of pets wear
              items is much less of a hassle, as body shapes are consistent.</p>

              <p style="text-indent: 15px">
              Obviously, TNT made a smart financial decision. The most coveted
              wearables can only be purchased through the NeoCash mall, which is
              the microtransaction hub on the site. Purchases of NeoCash keep
              Neopets afloat. People will readily drop up to thousands of
              dollars on buyables to customize their pets. It remains separate
              from the in-game currency, NeoPoints, which cannot be legitimately
              purchased, only earned by participating in site features. Think of
              NeoCash as currency used to purchase cosmetic modifiers, like
              skins in a game.</p>
              
