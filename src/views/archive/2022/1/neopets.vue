              clothes. Unconverted pets (UC for short) are highly coveted among
              Neopians, particularly Pound Chatters. UC pets find their values
              squeezing higher and higher due to their dwindling numbers;
              accounts are frozen, deleted, or just abandoned. </p>
              
              <p style="text-indent: 15px">
              People on the PC with an UC pet are treated with more respect. They are flaunted as
              a status symbol due to their grandfathered status - you cannot
              create an UC pet, you must either already have one, adopt one, or
              trade for one. If you paint an UC pet, it will convert to the new
              art. Users hand-code project pages for their UC trading goals,
              where ideal customizations, character stories, and artwork are
              fleshed out in HTML and CSS. Hundreds of applications flood in for
              the rare UC up for adoption, with applicants spending dozens of
              hours creating custom HTML/CSS applications in a bid for an UC
              dreamie.</p>
              
              <p style="text-indent: 15px">
              To navigate the tricky waters of the UC trading on the PC
              board, community-assigned pet values based on desirability and
              popularity are formalized in “tiers”. This information is hosted
