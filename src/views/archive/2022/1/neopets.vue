              named), DN (decently named), BN (badly named), and VBN (very badly
              named). There are also RWs (real words) and RNs (real names). RWs
              and RNs tend to be outliers. A word like “Decommissioned” won’t
              trade for a high-value pet, but a name like “Flora” or a word like
              “Pluto” are considered highly valuable due to character potential,
              and regularly trade for even the highest tier UCs. Common RWs and
              RNs are rare because only one pet can have one name at a time,
              much like how only one person can own a unique NFT at a time.</p> 
              
              <p style="text-indent: 15px">
              UCs may as well be unobtanium for many. Like NFTs, there were only
              ever a limited number of UCs in existence. Their value gets pushed
              higher based on speculative investment. The demand for them is
              ridiculously high, 1700s Dutch tulip mania level high, yet there
              are extraordinarily few ways to legitimately obtain them. These
              factors create a perfect environment for a black market to thrive.
              Black market sellers hack into inactive accounts and loot pets,
              items, and neopoints in exchange for real-life currency. UCs will
              sell anywhere from $20 to $500 on the black market depending on
              the rarity of the pet, and the pronounceability of the name.</p>

